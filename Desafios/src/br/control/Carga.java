package br.control;

public class Carga {
	
	public String valor;
	public String peso;
	
	
	
	public Carga(String valor, String peso) {
		this.valor = valor;
		this.peso = peso;
	}
	
	
	public String getValor() {
		return valor;
	}
	public void setValor(String valor) {
		this.valor = valor;
	}
	public String getPeso() {
		return peso;
	}
	public void setPeso(String peso) {
		this.peso = peso;
	}
	
	

}
